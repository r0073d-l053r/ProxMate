// Codegen: embed the ProxMate IDE config (ide/) into the backend as string
// constants, so the provisioner can generate a self-contained guest bootstrap
// without the ide/ dir being in the backend build context.
//
// Run from the dev machine (which has ide/) and COMMIT the output:
//   node scripts/gen-ide-assets.mjs
// The container build uses the committed src/services/ide-assets.generated.ts.
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import path from 'node:path';

const here = path.dirname(fileURLToPath(import.meta.url));
const ide = path.resolve(here, '../../ide');
const read = (rel) => readFileSync(path.join(ide, rel), 'utf8');

const settings = read('settings.json');
const autostartPkg = read('autostart/package.json');
const autostartJs = read('autostart/extension.js');

const out = `// AUTO-GENERATED by scripts/gen-ide-assets.mjs from ide/*. DO NOT EDIT.
// Re-run \`node scripts/gen-ide-assets.mjs\` after changing ide/settings.json or
// ide/autostart/*, then commit. Embedded so the guest provisioner is self-contained.
/* eslint-disable */
export const IDE_SETTINGS_JSON = ${JSON.stringify(settings)};
export const IDE_AUTOSTART_PACKAGE_JSON = ${JSON.stringify(autostartPkg)};
export const IDE_AUTOSTART_EXTENSION_JS = ${JSON.stringify(autostartJs)};
`;

const dest = path.resolve(here, '../src/services/ide-assets.generated.ts');
writeFileSync(dest, out);
console.log('wrote', path.relative(path.resolve(here, '..'), dest), `(${out.length} bytes)`);
